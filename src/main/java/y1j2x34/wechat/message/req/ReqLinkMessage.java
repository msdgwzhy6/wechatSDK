package y1j2x34.wechat.message.req;

import y1j2x34.wechat.utils.MessageUtils;

/**
 * 链接消息
 * <hr/>
 * @author 杨建新
 * @see ReqBaseMessage
 */
public class ReqLinkMessage extends ReqBaseMessage {

	private static final long serialVersionUID = -4998286396524518896L;
	
	private String Title;
	
	private String Description;
	
	private String Url;
	public ReqLinkMessage() {
		setMsgType(MessageUtils.REQ_MESSAGE_TYPE_LINK);
	}
	/**
	 * @return 消息标题
	 * @see #setTitle(String)
	 */
	public String getTitle() {
		return Title;
	}
	/**
	 * @param title
	 * @see #getTitle()
	 */
	public void setTitle(String title) {
		this.Title = title;
	}
	/**
	 * @return 消息描述
	 * @see #setDescription(String)
	 */
	public String getDescription() {
		return Description;
	}
	/**
	 * @param description
	 * @see #getDescription()
	 */
	public void setDescription(String description) {
		this.Description = description;
	}
	/**
	 * @return 消息链接
	 * @see #setUrl(String)
	 */
	public String getUrl() {
		return Url;
	}
	/**
	 * @param url
	 * @see #getUrl()
	 */
	public void setUrl(String url) {
		this.Url = url;
	}
}
