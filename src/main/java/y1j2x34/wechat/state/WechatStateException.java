package y1j2x34.wechat.state;

public class WechatStateException extends Exception{
	private static final long serialVersionUID = 1L;
	public WechatStateException() {
	}
	public WechatStateException(String message, Throwable cause) {
		super(message, cause);
	}
	public WechatStateException(String message) {
		super(message);
	}
	public WechatStateException(Throwable cause) {
		super(cause);
	}
	
}
