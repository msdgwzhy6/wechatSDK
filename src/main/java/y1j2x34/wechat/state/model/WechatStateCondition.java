package y1j2x34.wechat.state.model;

import java.io.Serializable;
/**
 * 状态转换条件
 * @author yangjianxin
 * @date 2014-12-9 上午8:21:07
 */
public class WechatStateCondition implements Serializable{
	private static final long serialVersionUID = 1L;
	private Integer conditonId;
	private String matcherName;
	private String value;
	public WechatStateCondition() {
	}
	public WechatStateCondition(Integer conditonId) {
		super();
		this.conditonId = conditonId;
	}
	public WechatStateCondition(Integer conditonId, String matcherName,
			String value) {
		super();
		this.conditonId = conditonId;
		this.matcherName = matcherName;
		this.value = value;
	}
	public Integer getConditonId() {
		return conditonId;
	}
	public void setConditonId(Integer conditonId) {
		this.conditonId = conditonId;
	}
	public String getMatcherName() {
		return matcherName;
	}
	public void setMatcherName(String matcherName) {
		this.matcherName = matcherName;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
}
