package y1j2x34.wechat.pojo;
/**
 * 自定义菜单基类
 * @see CommonButton
 * @see ComplexButton
 * @see ViewButton
 * @author 杨建新
 */
public class Button {
	private String name;
	/**
	 * 菜单标题，不超过16字节，子菜单不超过40字节
	 * @return
	 */
	public String getName() {
		return name;
	}
	/**
	 * @see #getName()
	 * @param name 不超过16字节
	 */
	public void setName(String name) {
		this.name = name;
	}
}
